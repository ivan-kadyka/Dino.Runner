namespace App.Character.Dino
{
    /// <summary>
    /// Enumerates the types of sounds associated with character actions or states
    /// </summary>
    internal enum CharacterSoundType
    {
        /// <summary>
        /// Sound played when the character jumps.
        /// </summary>
        Jump,

        /// <summary>
        /// Sound played when an effect is applied to the character.
        /// </summary>
        Effect,

        /// <summary>
        /// Sound played when the character is in an idle state.
        /// </summary>
        Idle
    }

}